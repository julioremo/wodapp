def build_keyword_processor() -> KeywordProcessor:
    """
    Returns a case-insensitive KeywordProcessor loaded with all aliases.
    """
    kp = KeywordProcessor(case_sensitive=False)
    kp.add_keywords_from_dict(EXERCISE_ALIASES)

    return kp


def normalize_text(raw: str, kp: KeywordProcessor) -> str:
    """Replace all known exercise aliases with canonical exercise_ids."""
    return kp.replace_keywords(raw)
